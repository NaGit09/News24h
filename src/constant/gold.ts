export function randomWithTrendSwitch(
  data: Record<string, GoldPrice[]>
) {
  const result: Record<string, GoldPrice[]> = {};

  Object.keys(data).forEach((brand) => {
    let currentBuy = data[brand][0].buy;

    result[brand] = data[brand].map((item, index) => {
      if (index !== 0) {
        // Xác suất tăng / giảm / đứng
        const r = Math.random();

        let delta = 0;

        if (r < 0.4) {
          // 40% tăng nhẹ
          delta = Math.random() * (0.35 - 0.05) + 0.05;
        } else if (r < 0.8) {
          // 40% giảm nhẹ
          delta = -(Math.random() * (0.35 - 0.05) + 0.05);
        } else {
          // 20% biến động mạnh (tin tức)
          delta = (Math.random() < 0.5 ? 1 : -1)
            * (Math.random() * (0.7 - 0.4) + 0.4);
        }

        // Nhiễu rất nhỏ
        delta += Math.random() * 0.1 - 0.05;

        currentBuy = +(currentBuy + delta).toFixed(1);
      }

      const spread = Math.random() * (2.4 - 1.8) + 1.8;

      return {
        ...item,
        buy: currentBuy,
        sell: +(currentBuy - spread).toFixed(1),
      };
    });
  });

  return result;
}

